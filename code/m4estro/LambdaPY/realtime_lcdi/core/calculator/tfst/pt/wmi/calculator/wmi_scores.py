import math

def compute_temp_score(x: float, a1: float, a2: float, k1: float, k2: float, c: float) -> float:
    if x >= c:
        return 1 / (1 + math.exp(-k1 * (x - a1)))
    
    return 1 / (1 + math.exp(k2 * (x - a2)))

